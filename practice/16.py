# Demonstrating for loop with range()

print("Numbers from 0 to 4:")
for i in range(5):
    print(i)

print("\nNumbers from 2 to 6:")
for i in range(2, 7):
    print(i)

print("\nEven numbers from 2 to 10:")
for i in range(2, 11, 2):
    print(i)

print("\nNumbers in reverse from 10 to 1:")
for i in range(10, 0, -1):
    print(i)
