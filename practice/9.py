# Integer
a = 10
print("Integer:", a, type(a))

# Float
b = 10.5
print("Float:", b, type(b))

# String
c = "Python"
print("String:", c, type(c))

# Boolean
d = True
print("Boolean:", d, type(d))

# List
e = [1, 2, 3, 4]
print("List:", e, type(e))

# Tuple
f = (1, 2, 3, 4)
print("Tuple:", f, type(f))

# Dictionary
g = {"name": "Alice", "age": 20}
print("Dictionary:", g, type(g))

# Set
h = {1, 2, 3, 4}
print("Set:", h, type(h))
