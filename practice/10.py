a, b = 15, 4

# Arithmetic
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)
print("Floor Division:", a // b)

# Relational
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)
print("a != b:", a != b)


# Assignment
c = 10
c += 5
print("Assignment (c += 5):", c)

# Logical
x, y = True, False
print("x and y:", x and y)
print("x or y:", x or y)
print("not x:", not x)



# Bitwise
print("Bitwise AND:", a & b)
print("Bitwise OR:", a | b)
print("Bitwise XOR:", a ^ b)
print("Bitwise NOT (~a):", ~a)
print("Bitwise Shift Left:", a << 1)
print("Bitwise Shift Right:", a >> 1)
